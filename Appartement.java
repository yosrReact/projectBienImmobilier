
public class Appartement {
    char Description;
	int Loc_Etage;
	int Ascenceur;
	int Cuisine_Equipee;

    public char getDescription() {
		return Description;
	}

	public void setDescription(char Description) {
		this.Description = Description;
	}

	public int getLoc_Etage() {
		return Loc_Etage;
	}

	public void setLoc_Etage(int Loc_Etage) {
		this.Loc_Etage = Loc_Etage;
	}

    public int getAscenceur() {
		return Ascenceur;
	}

	public void setAscenceur(int Ascenceur) {
		this.Ascenceur = Ascenceur;
	}

    public int getCuisine_Equipee() {
		return Cuisine_Equipee;
	}

	public void setCuisine_Equipee(int Cuisine_Equipee) {
		this.Cuisine_Equipee = Cuisine_Equipee;
	}
	
	
}
