public class Contrat {
	int noContrat;
	float prixEffectif;
	var dateEffectif;
	
	public int getNoContrat() {
		return this.noContrat;
	}

	public void setNoContrat(int noContrat) {
		this.noContrat = noContrat;
	}
	
	public float getPrixEffectif() {
		return this.prixEffectif;
	}

	public void setPrixEffectif(float prixEffectif) {
		this.prixEffectif = prixEffectif;
	}

	public var getDateEffectif() {
		return this.dateEffectif;
	}

	public void setDateEffectif(var dateEffectif) {
		this.dateEffectif = dateEffectif;
	}

}